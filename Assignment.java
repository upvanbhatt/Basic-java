class Assignment
{
    public static void main(String args[])
    {
        int a;
        a=10; // Simple ,here = is assignment operator
        System.out.println(a);
        System.out.println("Assignment Coumpound operator");
        a+=10; // (a=a+10)
        System.out.println(a);
        a-=10; // (a=a-10)
        System.out.println(a);

    }
}
    

